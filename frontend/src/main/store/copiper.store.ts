import Store from 'electron-store'

export interface LubanExportSettings {
  intermediateDataDir?: string
  intermediateSchemaDir?: string
  lubanDllPath?: string
  lubanConfPath?: string
  runLuban?: boolean
  outputCodeDir?: string
  outputDataDir?: string
  target?: string
  moduleName?: string
  codeTarget?: string
  dataTargets?: string[]
}

interface CopiperSchema {
  recentFiles: Record<string, string[]>
  exportSettings: {
    defaultFormats: ('python' | 'json' | 'luban')[]
    pythonHeader: string
    exportSubDir: string
    luban?: LubanExportSettings
  }
}

export const copiperStore = new Store<CopiperSchema>({
  name: 'copiper',
  schema: {
    recentFiles: {
      type: 'object',
      default: {}
    },
    exportSettings: {
      type: 'object',
      default: {
        defaultFormats: ['python'],
        pythonHeader:
          '# Auto generated by CoPiper, do not edit it. Unless you know what you are doing for sure.',
        exportSubDir: '',
        luban: {
          runLuban: true,
          moduleName: 'jdb',
          target: 'all',
          codeTarget: 'cpp-sharedptr-bin',
          dataTargets: ['bin', 'json']
        }
      },
      properties: {
        defaultFormats: { type: 'array', items: { type: 'string' } },
        pythonHeader: { type: 'string' },
        exportSubDir: { type: 'string' },
        luban: { type: 'object' }
      }
    }
  }
})

export function getRecentFiles(workspacePath: string): string[] {
  const all = copiperStore.get('recentFiles')
  return all[workspacePath] || []
}

export function addRecentFile(workspacePath: string, filePath: string): void {
  const all = copiperStore.get('recentFiles')
  const list = all[workspacePath] || []
  const filtered = list.filter((f) => f !== filePath)
  filtered.unshift(filePath)
  // Keep at most 20 recent files per workspace
  all[workspacePath] = filtered.slice(0, 20)
  copiperStore.set('recentFiles', all)
}

export function getExportSettings(): CopiperSchema['exportSettings'] {
  return copiperStore.get('exportSettings')
}

export function setExportSettings(
  settings: Partial<CopiperSchema['exportSettings']>
): void {
  const current = copiperStore.get('exportSettings')
  const next = {
    ...current,
    ...settings,
    luban: settings.luban
      ? { ...(current.luban || {}), ...settings.luban }
      : current.luban
  }
  copiperStore.set('exportSettings', next)
}

/** Translation function type returned by useT()/getT() */
export type TFunction = (key: string, ...args: string[]) => string

export function useT(): (key: string, ...args: (string | number)[]) => string {
  const language = useSettingsStore((state) => state.language) as Lang
  const lang = translations[language] ? language : 'zh-CN'
  return (key: string, ...args: (string | number)[]) => {
    let text = translations[lang][key] ?? key
    args.forEach((arg, i) => {
      text = text.replace(`{${i}}`, String(arg))
    })
    return text
  }
}

